# Подключаем тип Any, чтобы очередь могла хранить любые значения.
from typing import Any


# Создаем класс QueueList для структуры данных "очередь".
class QueueList:
    # Создаем метод запуска объекта.
    def __init__(self) -> None:
        # Создаем список, в котором будут лежать элементы очереди.
        self.items: list[Any] = []

    # Создаем метод проверки, пустая ли очередь.
    def is_empty(self) -> bool:
        # Возвращаем True, если элементов нет.
        return len(self.items) == 0

    # Создаем метод добавления элемента в конец очереди.
    def enqueue(self, item: Any) -> None:
        # Добавляем элемент в конец списка.
        self.items.append(item)

    # Создаем метод удаления первого элемента очереди.
    def dequeue(self) -> Any:
        # Проверяем, не пустая ли очередь.
        if self.is_empty():
            # Вызываем понятную ошибку, если очередь пустая.
            raise IndexError("Очередь пуста.")
        # Удаляем и возвращаем первый элемент списка.
        return self.items.pop(0)

    # Создаем метод просмотра первого элемента без удаления.
    def peek(self) -> Any:
        # Проверяем, не пустая ли очередь.
        if self.is_empty():
            # Вызываем понятную ошибку, если очередь пустая.
            raise IndexError("Очередь пуста.")
        # Возвращаем первый элемент списка.
        return self.items[0]

    # Создаем метод очистки очереди.
    def clear(self) -> None:
        # Полностью очищаем список.
        self.items.clear()

    # Создаем метод получения размера очереди.
    def size(self) -> int:
        # Возвращаем количество элементов.
        return len(self.items)

    # Создаем метод получения копии списка элементов.
    def to_list(self) -> list[Any]:
        # Возвращаем копию списка.
        return self.items.copy()

    # Создаем красивое строковое представление объекта.
    def __str__(self) -> str:
        # Возвращаем строку с текущими элементами очереди.
        return f"QueueList({self.items})"