# Подключаем тип Any, чтобы стек мог хранить любые значения.
from typing import Any


# Создаем класс Stack для структуры данных "стек".
class Stack:
    # Создаем метод запуска объекта.
    def __init__(self) -> None:
        # Создаем список, в котором будут лежать элементы стека.
        self.items: list[Any] = []

    # Создаем метод проверки, пустой ли стек.
    def is_empty(self) -> bool:
        # Возвращаем True, если в стеке нет элементов.
        return len(self.items) == 0

    # Создаем метод добавления элемента наверх стека.
    def push(self, item: Any) -> None:
        # Добавляем элемент в конец списка.
        self.items.append(item)

    # Создаем метод удаления верхнего элемента.
    def pop(self) -> Any:
        # Проверяем, не пустой ли стек.
        if self.is_empty():
            # Вызываем понятную ошибку, если стек пуст.
            raise IndexError("Стек пуст.")
        # Удаляем и возвращаем последний элемент списка.
        return self.items.pop()

    # Создаем метод просмотра верхнего элемента без удаления.
    def peek(self) -> Any:
        # Проверяем, не пустой ли стек.
        if self.is_empty():
            # Вызываем понятную ошибку, если стек пуст.
            raise IndexError("Стек пуст.")
        # Возвращаем последний элемент списка.
        return self.items[-1]

    # Создаем метод очистки стека.
    def clear(self) -> None:
        # Полностью очищаем список элементов.
        self.items.clear()

    # Создаем метод получения размера стека.
    def size(self) -> int:
        # Возвращаем количество элементов.
        return len(self.items)

    # Создаем метод получения копии списка элементов.
    def to_list(self) -> list[Any]:
        # Возвращаем копию списка, чтобы внешний код не ломал структуру напрямую.
        return self.items.copy()

    # Создаем красивое строковое представление объекта.
    def __str__(self) -> str:
        # Возвращаем строку с текущими элементами стека.
        return f"Stack({self.items})"